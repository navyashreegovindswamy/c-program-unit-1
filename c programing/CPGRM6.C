#include<stdio.h>
int main()
{
    short int a=32767;
    unsigned short int b=65535;
    long int c=123456789;
    signed long int d=-120;
    printf("Short int: %d\n",a);
    printf("Unsigned Short int: %u\n",b);
    printf("Long int: %ld\n",c);
    printf("Signed  int: %ld\n",d);
    return 0;
}
    